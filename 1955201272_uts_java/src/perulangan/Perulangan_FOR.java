package perulangan;


public class Perulangan_FOR {

    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 10; j++) {
                for (int k = 0; k < 100; k +=4) {
                    for (int l = 0; l < 100; l+=4) {
                        System.out.print(i+j+k+l+" ");
                    }System.out.println("");
                    
                  
                }
            }
                  
        }
        
       
        
    }
        
        
        
    
    
}
